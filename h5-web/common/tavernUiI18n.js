function touchLocaleTick() {
	try {
		return store && store.state ? store.state.localeTick : 0;
	} catch (e) {
		return 0;
	}
}

function getLanguageIndex() {
	touchLocaleTick();
	try {
		const raw = uni.getStorageSync('languageType');
		const codeMap = { 'zh-hk': 0, 'zh-cn': 1, en: 2, ko: 3, ja: 4 };
		if (raw === undefined || raw === null || raw === '') return 1;
		if (typeof raw === 'string' && codeMap[raw] !== undefined) return codeMap[raw];
		const n = Number(raw);
		if (isNaN(n)) return 1;
		return n >= 0 && n < LOCALE_CODES.length ? n : 1;
	} catch (e) {
		return 1;
	}
}

function getLanguageCode() {
	const idx = getLanguageIndex();
	return LOCALE_CODES[idx] || 'zh-cn';
}

